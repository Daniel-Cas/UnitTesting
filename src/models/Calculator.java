package models;


public class Calculator {

    public int add ( int a, int b) {
        return a + b;
    }

    public Integer degree( Integer a , Integer b){
        return a - b;
    }

    public Integer product( Integer a, Integer b){
        return a * b;
    }

    public Integer divition( Integer a, Integer b){
        return a / b;
    }



}
